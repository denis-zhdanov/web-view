package org.denis.webview.view;

/**
 * Enumerates possible view types.
 *
 * @author Denis Zhdanov
 * @since 21.06.2010
 */
public enum ViewType {

    /** Defines 'static' views, i.e. view that don't have dynamic content. */
    STATIC,

    /** Defines views that hold information about processed source of particular syntax. */
    SYNTAX
}
